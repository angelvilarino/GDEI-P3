def get_museum(room: Dict) -> Dict:
    museum_id = room["museumId"]
    for museum in MUSEUMS:
        if museum["id"] == museum_id:
            return museum
    raise KeyError(f"Museum not found for room {room['id']}")


def room_location(room: Dict) -> Dict:
    """Crea una ubicacion aproximada por sala basada en coordenadas del centro."""
    museum = get_museum(room)
    base_lon, base_lat = museum["location"]["coordinates"]
    token = sum(ord(c) for c in room["id"]) % 100
    offset_lon = ((token % 10) - 5) * 0.00003
    offset_lat = ((token // 10) - 5) * 0.00003
    return {
        "type": "Point",
        "coordinates": [round(base_lon + offset_lon, 7), round(base_lat + offset_lat, 7)],
    }


def room_code(room_id: str) -> str:
    return room_id.split(":")[-1]
